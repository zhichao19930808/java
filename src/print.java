public class print {
    public static void main(String[] args) {
        //print是直接输出
        //println是换行输出
        //转换字符\n是换行的意思；
        //转换字符\t是到下一个水平制表位置
        System.out.print("直接输出");
        System.out.println("换行输出");
        System.out.print("换行输出\n换行输出\n换行输出\n");
        System.out.print("到下个水平位置输出\t到下个水平位置输出\t到下个水平位置输出\t");
        //使用idea创建java应用程序，实线从控制台输出多行信息：姓名、年龄、爱好
        System.out.print("姓名\n");
        System.out.println("年龄");
        System.out.println("爱好");

    }
}
